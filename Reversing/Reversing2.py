# revese algorithm to decode password
def cmpToPsswd(bytes):
    psswd = []
    for byte in bytes:
        # for every byte add 0x77 and make sure it stays in the range 0x0-0xff
        psswd.append((byte+0x77) & 0xff)

    return psswd

# encoded password found in the binary
encodedPassword = [0xfc, 0xfd, 0xea, 0xc0, 0xba, 0xec, 0xe8, 0xfd, 0xfb, 0xbd, 0xf7,
         0xbe, 0xef, 0xb9, 0xfb, 0xf6, 0xbd, 0xc0, 0xba, 0xb9, 0xf7, 0xe8,
         0xf2, 0xfd, 0xe8, 0xf2, 0xfc, 0x00]

password = cmpToPsswd(encodedPassword)

print(bytearray(password).decode())
